# Input and Output Functions in Python

# What are Input and Output Functions?
'''
Input and Output functions are used to read and write data to and from files, databases, and user input.
There are several input and output functions in Python:
1. input(): This function is used to read input from the user. It returns a string.
2. print(): This function is used to print output to the console. It returns None.
'''

# ----------------------------
# 1. Input Function
# ----------------------------
'''
input() is a built-in function in Python that reads input from the user.
It always returns a string.
'''
# Example:
name = input("Enter your name: ")
print("\nHello, " + name)

# ----------------------------
# 2. Output Function
# ----------------------------
'''
print() is a built-in function in Python that prints output to the console.
It returns None.
'''
# Example:
print("\nI'm Python!")
