# Exceptional Handling in Python

# What is Exceptional Handling?
'''
In Python, an exception is an error that occurs during the execution of a program.
It is a runtime error that interrupts the normal flow of the program.

Hierarchy of Exceptions:
1. Zero-DivisionError
  It occurs when a number is divided by zero.

2. Name Error
It occurs when a name is not found. It may be local or Global.

3. IndentationError
It occurs when the indentation is not correct.

4. IOError
It occurs when the input-output operation fails.

Apart From the above, there are many more exceptions in Python.
'''

# How to Handle Exceptions?
'''
These exceptions can be handled by using the try-except block.

1. Try Block
It lets you test a block of code for errors.

2. Except Block
The except block lets you handle the error.

3. Else Block
The else block lets you execute code when there is no error.

4. Finally Block
The finally block lets you execute code, regardless of the result of the try- and except blocks.
'''